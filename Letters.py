import turtle
t=turtle.Turtle()
turtle.bgcolor("black")
t.pensize(5)
t.speed(1)
t.penup()
t.goto(-200,0)
t.pendown()
t.pencolor("orange")
t.forward(50)
t.right(180)
t.forward(100)
t.pensize(5)
t.penup()
t.goto(-200,0)
t.pendown()
t.pencolor("white")
t.left(90)
t.forward(50)
t.pencolor("green")
t.left(90)
t.forward(50)
t.right(180)
t.forward(100)
t.penup()
t.goto(0,0)
t.pendown()
t.left(90)
t.pencolor("orange")
t.forward(50)
t.penup()
t.goto(0,0)
t.pendown()
t.left(38)
t.pencolor("white")
t.forward(60)
t.left(142)
t.pencolor("green")
t.forward(50)
t.penup()
t.goto(170,0)
t.pendown()
t.right(180)
t.pencolor("orange")
t.forward(50)
t.left(90)
t.pencolor("white")
for i in range(45):
    t.forward(1)
    t.left(2.1)
t.pencolor("green")
for i in range(45):
    t.forward(1)
    t.left(2.1)
turtle.done()
